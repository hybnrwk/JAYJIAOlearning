# 验证汇率循环
# 货币：1=美元，2=欧元，3=英镑，4=日元

r = {
    (1, 2): 1.1486, (1, 3): 0.7003, (1, 4): 133.38,
    (2, 1): 0.8706, (2, 3): 0.6097, (2, 4): 116.12,
    (3, 1): 1.4279, (3, 2): 1.6401, (3, 4): 190.45,
    (4, 1): 0.00750, (4, 2): 0.00861, (4, 3): 0.00525
}

currency_names = {1: "美元", 2: "欧元", 3: "英镑", 4: "日元"}

print("验证各种兑换循环：\n")

# 测试各种可能的循环
cycles = [
    [(2, 1), (1, 2)],  # 欧元→美元→欧元
    [(2, 3), (3, 2)],  # 欧元→英镑→欧元
    [(2, 4), (4, 2)],  # 欧元→日元→欧元
    [(2, 1), (1, 3), (3, 2)],  # 欧元→美元→英镑→欧元
    [(2, 1), (1, 4), (4, 2)],  # 欧元→美元→日元→欧元
    [(2, 3), (3, 4), (4, 2)],  # 欧元→英镑→日元→欧元
    [(2, 3), (3, 1), (1, 2)],  # 欧元→英镑→美元→欧元
    [(2, 4), (4, 1), (1, 2)],  # 欧元→日元→美元→欧元
    [(2, 1), (1, 3), (3, 4), (4, 2)],  # 欧元→美元→英镑→日元→欧元
]

for cycle in cycles:
    result = 1.0
    path = "1 欧元"
    for (i, j) in cycle:
        result *= r[(i, j)]
        path += f" → {result:.6f} {currency_names[j]}"
    
    print(f"{path}")
    print(f"  最终结果：{result:.8f}，{'套利！' if result > 1.0001 else '无套利'}\n")

print("\n" + "="*60)
print("结论：")
if any(all((i,j) in r for (i,j) in cycle) and 
       all(r[(i,j)] for (i,j) in cycle) and
       (lambda c: (result := 1.0, [result := result * r[(i,j)] for (i,j) in c], result > 1.0001)[-1])(cycle)
       for cycle in cycles):
    print("存在套利循环！")
else:
    print("不存在明显的套利循环。")
